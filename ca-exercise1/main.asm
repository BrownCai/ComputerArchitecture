	rlc PD_ODR
	ld A, PD_ODR
	cp A, #$c0
	jrne shiftLeft
shiftRight
	call delay_1s
	rrc PD_ODR
	ld A, PD_ODR
	cp A, #3
	jrne shiftRight
	
	jra infinite_loop
	

delay_1s
	call delay
	inc A
	cp A, #$ff
	jrc delay_1s
	inc 0
	ld A, 0
	cp A, #$ff
	ld A, #0
	jrc delay_1s
